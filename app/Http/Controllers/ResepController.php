    {
        $post = new Resep();
        $post ->idproduk = $request->get('inputIdProduk');
        $post ->idbahan_baku = $request->get('inputIdBB');
        $post ->jumlah = $request->get('inputJml');
        $post->save();
        return redirect('reseps');
    }

